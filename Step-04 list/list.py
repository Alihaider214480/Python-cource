fruits=["apple","banana","kiwi"]
#first method is to check length using "len method"
print(len(fruits))
#checking using index method
print(fruits[0])
#adding item in list using "append method"
fruits.append("oranges")
print(fruits)
#removing item using removing method
fruits.remove("oranges")
print(fruits)
#modifying list
fruits[1]="guava"
print(fruits)
#adding item at choice loc
fruits.insert(2,"mango")
print(fruits)